// for, while
let alunos = ['Leandro', 'Luciana', 'Pedro', 'Beatriz']

// utilizar o for quando já souber a condição final
for (let res = 0; res <= alunos.length; res++) {
    console.log(res)
}

// utilizamos o while quando não soubemos a condição final
let result = 23456789
while (result > 50) {
    console.log(result)

    result /= 10
}